t0 = Time.now
palin_sum = 0

1.upto(1_000_000) do |number|
bin_number = number.to_s(2).to_i
palin_sum += number if number.to_s.reverse.to_i == number and  bin_number.to_s.reverse.to_i == bin_number
end

p palin_sum
p Time.now - t0



